<p> 嗜心狱妖前肢颀长，只有两指却锋利无比。</p>
<p> 它准确的洞察到'Mo Fan'遁影的位置，朝着'Mo Fan'就是猛的一撕，要将化作影子的'Mo Fan'给直接撕成两半！</p>
<p> 'Mo Fan'意识到自己根本避不开这家伙，急忙凝聚出一面意念之墙来，抵挡这个怪物的撕心厉爪。</p>
<p> 意念形成的防御之墙根本抵挡不了嗜心狱妖的攻击，'Mo Fan'生生的扭转，臂开自己的要害。</p>
<p> 下一秒，一阵剧痛从胳膊的位置传了过来，火辣辣的，能够清楚的感觉到身体里的血液在朝着空气中滚烫喷洒！！</p>
<p> 'Mo Fan'倒吸一口气，就看见自己的半条胳膊飞离自己身体，鲜血淋漓的拍落在了旁边的石壁上。</p>
<p> 顾不得疼痛，'Mo Fan'用意念将自己那条手臂给抓取了回来，随后立刻以火焰化羽，开启了火翼喷射，以最快的速度远离这个可怕至极的生物！！</p>
<p> “千层火羽！！”</p>
