<p> 山壁下层被'Mo Fan'炸断，于是两座山壁轰然倒塌，无数的断石堆砌在这并不宽敞的长沟之谷中，形成了一个山峡堤坝，一下子将'Mo Fan'和那怪物给隔绝了开！</p>
<p> 地动山摇之间，'Mo Fan'落到了'Mu Ning Xue'、托尼、艾琳三人的面前。</p>
<p> 艾琳看到'Mo Fan'竟然用一只手握着另外一半鲜血淋漓的手，脸上不满了震惊之色。</p>
<p> 一方面是那怪物竟然可以在这么短的交锋中就断了'Mo Fan'一条手臂，另一方面更惊叹'Mo Fan'这种生死迂回中的镇定，镇定得让艾琳都怀疑这家伙是没有痛觉，没有恐惧……</p>
<p> “那断山拖不住它太久，我们快走。”'Mo Fan'拧着血淋漓的手，领着他们继续逃亡。</p>
