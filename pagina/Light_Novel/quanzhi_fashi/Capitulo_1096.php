<p>雷电与火焰共同在周身交织，组成了一个强大无比的雷火双莲，手中更握着烈火炎剑，'Mo Fan'迎面朝着那嗜心狱妖斩去！！</p>
<p> 嗜心狱妖伸出了厉爪，原本的飞行轨迹是会与'Mo Fan'在半空中相撞，可是这家伙忽然在半空中进行了幻影横移，'Mo Fan'那凌厉的一剑就斩在了一抹梭织之丝上，根本没有劈中那个家伙！</p>
<p> “呷~~~~~~~~~~~~！！！！”</p>
<p> 嗜心狱妖出现在'Mo Fan'的身侧，'Mo Fan'反应还算很快，苍雷爪落下，直击这空中爬行的怪物。</p>
<p> 哪知嗜心狱妖相当狡猾，这依旧是它的佯攻，它忽然一跳，身子敏捷的飞到了'Mo Fan'的正后方。</p>
<p> 在正后方的位置上，这嗜心狱妖猛的一踏空气，身体竟然接着空气的力量猛的弹射，以惊人的速度冲向'Mo Fan'身后！！</p>
<p> 'Mo Fan'惊出一声冷汗，自己已经算是留了一手，哪知道嗜心狱妖会这般的狡猾，一连变幻了两三个位置不说，更是可以在踱步到自己身后的那瞬间在空中爆发诡诈速度，这速度快到让'Mo Fan'转身的时间都没有，毕竟'Mo Fan'才刚刚释放苍雷爪！！</p>
<p> “遁影！！”</p>
<p> 嗜心狱妖是在'Mo Fan'的死角，'Mo Fan'根本就看不见那家伙，但他凭借着直觉去闪避。</p>
<p> 空中滑落，'Mo Fan'迅速的沉入到了黑色的影子里，五级的遁影使得他一下子分化出了几个假影，并利用暗爵斗篷在原地留下了一个暗影傀儡！</p>
<p> “呷~~~~~~~~！！！！！”</p>
<p> 嗜心狱妖发出了尖锐的叫声，却完全识破了'Mo Fan'这点黑暗手段。</p>
