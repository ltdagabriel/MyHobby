<p> 所以，'Zhao Man Yan'得到的这视频画面，百分之一万是某些有心人用特殊的军用仪器录下来的，一旦被发现，圣裁院会毫不留情的处置！</p>
<p> “好像是某个圣裁人员自己偷偷录的，这个角度看不到埃森德尔啊。”'Mo Fan'盯着那不断有干扰的画面，自言自语道。</p>
