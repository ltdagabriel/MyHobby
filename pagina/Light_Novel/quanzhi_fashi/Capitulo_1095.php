<p> “弓！！”</p>
<p> 'Mu Ning Xue'不敢有半点耽搁，死亡已经在敲门了，若是让那些家伙率先出手，他们几个人连还手的余地都没有就会全部被挂在这里变成它们的战利品！</p>
<p> 磐冰领域以几倍的冰寒之威豁然笼罩在了这个深渊龙潭之中，空气之中都可以看见一道道如梅花一般的冰霜之枝在蔓延，在编织，在凝固！</p>
