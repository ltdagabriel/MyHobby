<p> 死神毒妖在它们的脑袋上不断的踱步，谁都可以看出来这嗜心狱妖实力绝对比他们强很多，任何一只都可以轻易的将他们如猪样一样宰割，更不用说是石壁上还趴着另外一只！！</p>
<p> “先发制人！”'Mo Fan'已经察觉到不对劲了，急忙对'Mu Ning Xue'说道。</p>
<p> 银丝飞舞，衣襟瑟瑟，'Mu Ning Xue'气势狂然的舒展开双臂，曲线的身姿竟然微微浮在空中！！</p>
<p> 钻石粉尘在'Mu Ning Xue'的面前凝聚，冰晶刹弓那妙曼弓身渐渐的勾勒呈现，长长的水晶箭矢被'Mu Ning Xue'捏在指缝间！</p>
<p> “呷！！！！！”</p>
<p> 嗜心狱妖终于意识到这几个人类并非是之前那些可以任意蹂躏的猎物，它身影如梦如幻，寻找了一个刁钻的角度朝着'Mu Ning Xue'扑了过去，修长尖锐的前爪豁然张开，直逼'Mu Ning Xue'咽喉要害！！</p>
<p> “粉碎箭！！”</p>
<p> 气势达到了临界点，一切都在这一刻停止、寂然，唯有一杆冷酷冰艳的箭矢撕破了时间的凝固，卷起了一场震撼的粉碎风暴……</p>
<p> 粉碎水晶箭迎着嗜心狱妖而去，嗜心狱妖在飞扑的过程中身后更是笼着浓浓的毒浪，黑压压一大片，铺天盖地！</p>
<p> 这种毒之气势与'Mu Ning Xue'的冰雪圣洁风暴形成了鲜明的对比，正好一半充斥在整个龙潭左边，一半弥漫在龙潭右边，碰撞的那一刻，整个龙潭剧烈的晃动了起来！！！</p>
<p> “咻！！！”</p>
<p> 箭与嗜心狱妖在半空中相撞，宛如两军的主帅纵马相迎，箭飞逝而过，直击嗜心狱妖的怪人身躯！！</p>
