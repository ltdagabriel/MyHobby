<p> 嗜心狱妖没有眼睛，可却总能够感觉到它们贪婪阴毒的凝视，这只嗜心狱妖死死的锁着'Mu Ning Xue'，竟然一下子爬了下来！</p>
<p> 它爬行的速度极快，明明没有任何的道路，它竟然在空气中任意的行走，时而如蜘蛛蛛网横向踏步，时而如蝎那般快步疾冲，又时而如蛇那般盘绕行走……</p>
<p> 似乎带着忌惮，它并没有冒然的攻击'Mu Ning Xue'，而是不断的在他们几人的头顶上变幻位置。</p>
<p> 'Mo Fan'就看见一道道邪异的残影不断的闪过，速度快得完全看不清其动作，如果无法判定妖魔的行走轨迹的话那是相当危险的！</p>
