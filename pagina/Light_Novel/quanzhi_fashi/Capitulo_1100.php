<p> “第四名是一个铁桶法师，美国的毕尔顿，人高马大，主修岩系、辅修-Light Magic-、次修-Call-up Magic-。防御型法师，在淘汰赛上，这家伙被瑞典的五个人围攻，打了半天都不见他的防御破碎，是毁灭法师的大克星！”江昱说道手机用户请浏览阅读，更优质的阅读体验。<br />
