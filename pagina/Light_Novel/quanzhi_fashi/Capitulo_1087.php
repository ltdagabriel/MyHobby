<p> 满是利刺的四条臂膀连续打落下来，将地表扎得千疮百孔，在这攻击之下，'Mo Fan'能够闪躲的区域非常有限，那些毒刺相当的密，又附带着可怕的毒性，'Mo Fan'的皮肤被稍稍刮到，伤口便立刻蔓成了一条狭长的痕，宛如刀砍之伤。</p>
