<p> 寒芒破空，直逼'Mo Fan'胸膛，'Mo Fan'惊出了一声冷汗，这袭击是他完全没有预料到的，眼见那毒蝎之刺就在面前，'Mo Fan'却是连一点防御手段都没有！</p>
<p> 玄蛇铠甲已经用过了，冥离盾更是在不久前化作了碎片，临时铸出空间之墙是来不及了，要闪躲更没有半点余地，对方四条手臂完全就是封死了自己所有逃跑路线！！</p>
<p> 避无可避，防无可防，'Mo Fan'有那么一刹那觉得自己是要被这毒刺给贯穿胸口了……</p>
<p> 亚克这家伙倒比安吉洛那小人正直一点点，他并没有刺自己心脏那一侧，只是接下去一样会凄惨无比！</p>
<p> 冷意袭来，'Mo Fan'猛的一咬舌，暗骂自己还会去庆幸这只是比赛，出生入死无数次，法师的生存之道里是没有侥幸可言的！！</p>
<p> 不到最后，都不能就这样束手，不是还有念控吗！</p>
<p> 空间系的初阶魔法念控只需要一个眼神就可以完成，这凌厉致命毒刺再快难道可以快过自己的眼神！！</p>
<p> “念控！！！！！”</p>
<p> 双瞳豁然银色光辉绽放，'Mo Fan'没有避，也没有防，竟然是用自己的目光去迎接着闪电袭来的致命毒刺。</p>
<p> 艾江图可以做到将漫天的魔法轰炸都化作静止，自己为什么不可以阻挡下这致命的攻击，意念不可动摇，心脏必须足够强大，空间系的强与弱不取决于技能，不取决于魂种，只在于自身有着无可披靡的精神力！！</p>
